package peripheral.logic.datatype;


public class PointF {

    private float x;

    private float y;

    public PointF () {
    }

    public float getX () {
        return x;
    }

    public void setX (float val) {
        this.x = val;
    }

    public float getY () {
        return y;
    }

    public void setY (float val) {
        this.y = val;
    }

}

